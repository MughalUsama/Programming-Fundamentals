#include <iostream>
using namespace std;
int main ()
{
	float pi, radius;
	pi = 3.14159;
	radius = 11.33;
	cout << "Diameter of circle is: " <<  2 * radius << "\n";
	cout << "Circumference of circle is: " << 2 * pi * radius << "\n";
	cout << "Area of circle is: " << pi * radius * radius << "\n";
	cout << "\n";
	system("pause");
	return 0;

}